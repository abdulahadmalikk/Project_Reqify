W1, W2, W3 = 0.40, 0.35, 0.25
TOL = 0.005

def compute_risk(dep, amb, vol):
    idx = W1*amb + W2*vol + W3*dep
    lvl = "HIGH" if idx >= 0.66 else ("MEDIUM" if idx >= 0.36 else "LOW")
    return round(idx, 3), lvl

