@pytest.mark.parametrize(
    "tc_id, description, requirement_text, expected_prediction",
    TEST_CASES,
    ids=[tc[0] for tc in TEST_CASES],
)
def test_fr_nfr_classification(
    classifier, tc_id, description, requirement_text, expected_prediction
):
    """
    Directly calls the loaded RoBERTa model (no API).
    Asserts that the model prediction matches the expected FR/NFR label.
    """
    tokenizer, model, labels, device = classifier
    actual = predict(requirement_text, tokenizer, model, labels, device)

    assert actual == expected_prediction, (
        f"\n[{tc_id}] {description}\n"
        f"  Requirement : {requirement_text}\n"
        f"  Expected    : {expected_prediction}\n"
        f"  Got         : {actual}"
    )
