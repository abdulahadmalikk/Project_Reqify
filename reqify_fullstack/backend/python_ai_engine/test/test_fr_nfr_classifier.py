# ---------------------------------------------------------------------------
# Helper used by every test
# ---------------------------------------------------------------------------
def predict(text: str, tokenizer, model, labels, device) -> str:
    """Run inference and return 'FR' or 'NFR'."""
    inputs = tokenizer(
        text,
        return_tensors="pt",
        truncation=True,
        padding=True,
        max_length=128
    ).to(device)

    with torch.no_grad():
        outputs = model(**inputs)
        logits = outputs.logits

    probs = torch.softmax(logits, dim=1)
    pred_index = torch.argmax(probs, dim=1).item()
    return labels[pred_index]


