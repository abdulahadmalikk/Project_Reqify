class DynamicUnitConverter:
    """Dynamic unit conversion using Pint library"""

    def __init__(self):
        self.ureg = UnitRegistry()
        self.debug = False

    def extract_value_unit(self, text: str) -> Optional[Tuple[float, str]]:
        """Extract value and unit from text - IMPROVED"""
        text_lower = text.lower()

        patterns = [
            # Time units
            (r'(\d+\.?\d*)\s*(millisecond|milliseconds|ms)\b', 'millisecond'),
            (r'(\d+\.?\d*)\s*(second|seconds|sec|secs|s)\b', 'second'),
            (r'(\d+\.?\d*)\s*(minute|minutes|min|mins|m)\b', 'minute'),
            (r'(\d+\.?\d*)\s*(hour|hours|hr|hrs|h)\b', 'hour'),
            (r'(\d+\.?\d*)\s*(day|days|d)\b', 'day'),

            # Data units - FIXED: Check MB/GB before just B
            (r'(\d+\.?\d*)\s*(terabyte|terabytes|tb|tbs)\b', 'terabyte'),
            (r'(\d+\.?\d*)\s*(gigabyte|gigabytes|gb|gbs)\b', 'gigabyte'),
            (r'(\d+\.?\d*)\s*(megabyte|megabytes|mb|mbs)\b', 'megabyte'),
            (r'(\d+\.?\d*)\s*(kilobyte|kilobytes|kb|kbs)\b', 'kilobyte'),
            (r'(\d+\.?\d*)\s*(?<!kilo)(?<!mega)(?<!giga)(?<!tera)(byte|bytes|b)\b', 'byte'),

            # Currency
            (r'\$(\d+\.?\d*)', 'USD'),
        ]

        for pattern, standard_unit in patterns:
            match = re.search(pattern, text_lower, re.IGNORECASE)
            if match:
                value = float(match.group(1))
                if self.debug:
                    print(f"  Extracted: {value} {standard_unit} from '{text}'")
                return value, standard_unit

        return None

    def are_equivalent(self, value1: float, unit1: str, value2: float, unit2: str,
                      tolerance: float = 0.001) -> bool:
        """Check if two value-unit pairs are equivalent - FIXED FOR BINARY UNITS"""
        try:
            if self.debug:
                print(f"\n  Comparing: {value1} {unit1} vs {value2} {unit2}")

            # CRITICAL: Handle data units with BINARY conversion (1024-based)
            data_units = ['byte', 'kilobyte', 'megabyte', 'gigabyte', 'terabyte',
                         'kb', 'mb', 'gb', 'tb', 'bytes', 'kilobytes', 'megabytes', 'gigabytes', 'terabytes']
            time_units = ['second', 'minute', 'hour', 'day', 'millisecond',
                         'seconds', 'minutes', 'hours', 'days', 'milliseconds']

            is_data = any(u in unit1.lower() for u in data_units)
            is_time = any(u in unit1.lower() for u in time_units)

            if is_data:
                # MANUAL BINARY CONVERSION (1024-based for computer storage)
                def to_bytes_binary(value, unit):
                    """Convert to bytes using binary (1024) multipliers"""
                    unit_lower = unit.lower()
                    if 'terabyte' in unit_lower or unit_lower == 'tb':
                        return value * 1024 * 1024 * 1024 * 1024
                    elif 'gigabyte' in unit_lower or unit_lower == 'gb':
                        return value * 1024 * 1024 * 1024
                    elif 'megabyte' in unit_lower or unit_lower == 'mb':
                        return value * 1024 * 1024
                    elif 'kilobyte' in unit_lower or unit_lower == 'kb':
                        return value * 1024
                    else:  # bytes
                        return value

                bytes1 = to_bytes_binary(value1, unit1)
                bytes2 = to_bytes_binary(value2, unit2)

                if self.debug:
                    print(f"  Binary conversion (1024-based):")
                    print(f"    {value1} {unit1} = {bytes1} bytes")
                    print(f"    {value2} {unit2} = {bytes2} bytes")

                diff = abs(bytes1 - bytes2)

                if self.debug:
                    print(f"  Difference: {diff} bytes")

                # Allow small tolerance (< 1 byte)
                return diff < 1.0

            elif is_time:
                # Use Pint for time conversions (decimal is fine)
                q1 = self.ureg.Quantity(value1, unit1)
                q2 = self.ureg.Quantity(value2, unit2)

                q1_base = q1.to('second')
                q2_base = q2.to('second')

                if self.debug:
                    print(f"  In seconds: {q1_base} vs {q2_base}")

                diff = abs(q1_base.magnitude - q2_base.magnitude)

                if self.debug:
                    print(f"  Difference: {diff} seconds")

                return diff < 0.01

            else:
                # General comparison using Pint
                q1 = self.ureg.Quantity(value1, unit1)
                q2 = self.ureg.Quantity(value2, unit2)

                q2_converted = q2.to(q1.units)
                diff = abs(q1.magnitude - q2_converted.magnitude)
                relative_diff = diff / max(abs(q1.magnitude), abs(q2_converted.magnitude), 0.0001)

                if self.debug:
                    print(f"  Relative difference: {relative_diff}")

                return relative_diff < tolerance

        except Exception as e:
            if self.debug:
                print(f"  ERROR: {e}")
            return False

    def convert(self, value: float, from_unit: str, to_unit: str) -> Optional[float]:
        """Convert value from one unit to another"""
        try:
            quantity = self.ureg(f"{value} {from_unit}")
            converted = quantity.to(to_unit)
            return converted.magnitude
        except Exception:
            return None

