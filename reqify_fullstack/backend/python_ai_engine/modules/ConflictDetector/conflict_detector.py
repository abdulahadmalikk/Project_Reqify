class DuplicateStrategy4_DomainSynonymDetection(DetectionStrategy):
    """ENHANCED: Domain-aware synonym detection with strict filtering"""

    def detect(self, req1, req2, context):
        nouns1 = set(self.nlp_utils.extract_nouns(req1))
        nouns2 = set(self.nlp_utils.extract_nouns(req2))
        verbs1 = set(self.nlp_utils.extract_verbs(req1))
        verbs2 = set(self.nlp_utils.extract_verbs(req2))

        synonym_matches = []

        # Check noun synonyms
        for noun1 in nouns1:
            for noun2 in nouns2:
                if noun1 == noun2:
                    continue
                if self.nlp_utils.are_synonyms(noun1, noun2):
                    synonym_matches.append(('noun', noun1, noun2))

        # Check verb synonyms
        for verb1 in verbs1:
            for verb2 in verbs2:
                if verb1 == verb2:
                    continue
                if self.nlp_utils.are_synonyms(verb1, verb2):
                    synonym_matches.append(('verb', verb1, verb2))

        if synonym_matches:
            num_matches = len(synonym_matches)
            sim = context['cosine_sim']

            # IMPROVED: More lenient for strong domain matches
            # If we have 2+ synonym matches from domain knowledge, accept with lower similarity
            # If only 1 match, require high similarity
            if num_matches >= 2:
                min_similarity = 0.60  # Lower threshold for multiple matches
            else:
                min_similarity = 0.75  # Higher threshold for single match

            if sim < min_similarity:
                return False, 0, {}

            # Check for conflicting modifiers
            if self.has_conflicting_modifiers(req1, req2):
                return False, 0, {}

            # Check for negation patterns
            if self.has_negation_conflict(req1, req2):
                return False, 0, {}

            confidence = min(0.90, sim + 0.10 + (num_matches * 0.05))
            return True, confidence, {
                'method': 'domain_synonym_match',
                'matches': [f"{m[1]}↔{m[2]}" for m in synonym_matches],
                'count': num_matches,
                'domain': self.nlp_utils.domain
            }

        return False, 0, {}

    def has_conflicting_modifiers(self, req1, req2):
        """Check for conflicting adjectives/determiners"""
        doc1 = self.nlp_utils.nlp(req1)
        doc2 = self.nlp_utils.nlp(req2)

        modifiers1 = [token.text.lower() for token in doc1 if token.pos_ in ['DET', 'ADJ']]
        modifiers2 = [token.text.lower() for token in doc2 if token.pos_ in ['DET', 'ADJ']]

        for mod1 in modifiers1:
            for mod2 in modifiers2:
                if self.nlp_utils.are_antonyms(mod1, mod2):
                    return True
        return False

    def has_negation_conflict(self, req1, req2):
        """Check if one requirement has negation and other doesn't"""
        negation_words = ['not', 'no', 'never', 'cannot', 'must not', 'mustn\'t']

        has_neg1 = any(neg in req1.lower() for neg in negation_words)
        has_neg2 = any(neg in req2.lower() for neg in negation_words)

        # If one has negation and other doesn't, it's likely a conflict
        return has_neg1 != has_neg2

