class InconsistencyStrategy5_MathematicalConsistency(DetectionStrategy):
    """Check mathematical relationships"""

    def extract_price_info(self, req):
        req_lower = req.lower()
        price_match = re.search(r'\$(\d+\.?\d*)', req)
        if not price_match:
            return None, None

        price = float(price_match.group(1))

        if 'month' in req_lower or 'monthly' in req_lower:
            period = 'month'
        elif 'year' in req_lower or 'annual' in req_lower or 'yearly' in req_lower:
            period = 'year'
        else:
            period = None

        return price, period

    def detect(self, req1, req2, context):
        price1, period1 = self.extract_price_info(req1)
        price2, period2 = self.extract_price_info(req2)

        if not (price1 and price2 and period1 and period2):
            return False, 0, {}

        if period1 == period2:
            return False, 0, {}

        if period1 == 'month' and period2 == 'year':
            expected_annual = price1 * 12
            if abs(expected_annual - price2) > (expected_annual * 0.15):
                return True, 0.75, {
                    'method': 'mathematical_inconsistency',
                    'monthly': price1,
                    'annual': price2,
                    'expected_annual': expected_annual
                }

        return False, 0, {}

