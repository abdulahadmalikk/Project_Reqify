        numbers_1 = [token for token in doc1 if token.like_num]
        numbers_2 = [token for token in doc2 if token.like_num]

        is_vague_1 = has_vague_1 and len(numbers_1) == 0
        is_vague_2 = has_vague_2 and len(numbers_2) == 0
        is_specific_1 = len(numbers_1) > 0
        is_specific_2 = len(numbers_2) > 0

        if (is_vague_1 and is_specific_2) or (is_vague_2 and is_specific_1):
            if context['cosine_sim'] > 0.5:
                return True, 0.70, {
                    'method': 'vague_vs_specific',
                    'vague_req': req1 if is_vague_1 else req2,
                    'specific_req': req2 if is_vague_1 else req1
                }

        return False, 0, {}

