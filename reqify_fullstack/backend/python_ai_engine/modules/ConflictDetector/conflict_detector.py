class DomainAwareNLPUtils:
    """Domain-aware NLP utilities with enhanced synonym detection"""

    def __init__(self, domain: str = 'ecommerce'):
        try:
            self.nlp = spacy.load('en_core_web_sm')
        except:
            print("Downloading spaCy model...")
            import os
            os.system('python -m spacy download en_core_web_sm')
            self.nlp = spacy.load('en_core_web_sm')

        self.domain = domain
        self.domain_knowledge = DomainKnowledge()

    def set_domain(self, domain: str):
        """Change the active domain"""
        self.domain = domain

    def get_synonyms(self, word: str) -> Set[str]:
        """Get synonyms using both WordNet AND domain knowledge"""
        synonyms = set()

        # WordNet synonyms
        for syn in wordnet.synsets(word):
            for lemma in syn.lemmas():
                synonyms.add(lemma.name().replace('_', ' '))

        # Domain-specific synonyms (PRIORITY)
        domain_syns = self.domain_knowledge.get_domain_synonyms(self.domain)
        word_normalized = word.lower().replace(' ', '_')

        for syn_group, category in domain_syns.items():
            if word_normalized in syn_group:
                # Add all words from the same group
                synonyms.update([w.replace('_', ' ') for w in syn_group])

        return synonyms

    def are_synonyms(self, word1: str, word2: str, threshold: float = 0.7) -> bool:
        """Check if two words are synonyms (domain-aware)"""
        word1 = word1.lower().strip()
        word2 = word2.lower().strip()

        if word1 == word2:
            return True

        # PRIORITY: Check domain knowledge first
        if self.domain_knowledge.is_domain_synonym(word1, word2, self.domain):
            return True

        # Fallback to WordNet
        syns1 = self.get_synonyms(word1)
        syns2 = self.get_synonyms(word2)

        if word2 in syns1 or word1 in syns2:
            return True

        if syns1 and syns2:
            overlap = len(syns1.intersection(syns2))
            total = len(syns1.union(syns2))
            if total > 0 and (overlap / total) > threshold:
                return True

        return False

    def extract_nouns(self, text: str) -> List[str]:
        """Extract nouns dynamically"""
        doc = self.nlp(text)
        return [token.lemma_ for token in doc if token.pos_ in ['NOUN', 'PROPN']]

    def extract_verbs(self, text: str) -> List[str]:
        """Extract verbs dynamically"""
        doc = self.nlp(text)
        return [token.lemma_ for token in doc if token.pos_ == 'VERB']

    def get_antonyms(self, word: str) -> Set[str]:
        """Get antonyms using both WordNet AND domain knowledge"""
        antonyms = set()

        # WordNet antonyms
        for syn in wordnet.synsets(word):
            for lemma in syn.lemmas():
                if lemma.antonyms():
                    antonyms.add(lemma.antonyms()[0].name())

        # Domain-specific antonyms (PRIORITY)
        domain_ants = self.domain_knowledge.get_domain_antonyms(self.domain)
        word_normalized = word.lower().replace(' ', '_')

        for pair, _ in domain_ants.items():
            if word_normalized in pair[0]:
                antonyms.add(pair[1].replace('_', ' '))
            elif word_normalized in pair[1]:
                antonyms.add(pair[0].replace('_', ' '))

        return antonyms

    def are_antonyms(self, word1: str, word2: str) -> bool:
        """Check if two words are antonyms (domain-aware)"""
        # PRIORITY: Check domain knowledge first
        if self.domain_knowledge.is_domain_antonym(word1, word2, self.domain):
            return True

        # Fallback to WordNet
        antonyms1 = self.get_antonyms(word1)
        antonyms2 = self.get_antonyms(word2)
        return word2 in antonyms1 or word1 in antonyms2

    def extract_semantic_keywords(self, text: str) -> Set[str]:
        """Extract semantically important keywords"""
        doc = self.nlp(text)
        keywords = set()
        for token in doc:
            if token.pos_ in ['NOUN', 'PROPN', 'VERB', 'ADJ'] and not token.is_stop:
                keywords.add(token.lemma_.lower())
        return keywords

