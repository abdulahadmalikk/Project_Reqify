class MultiStrategyDetector:
    def __init__(self, domain: str = 'auto'):
        print("Initializing domain-aware NLP utilities...")
        self.domain = domain
        self.nlp_utils = DomainAwareNLPUtils('ecommerce')  # Default

        print("Initializing unit converter (Pint)...")
        self.unit_converter = DynamicUnitConverter()

        # Strategy lists
        self.duplicate_strategies = [
            DuplicateStrategy3_DynamicUnitConversion(self.nlp_utils, self.unit_converter),
            DuplicateStrategy1_SemanticSimilarity(self.nlp_utils, self.unit_converter),
            DuplicateStrategy2_NLIEntailment(self.nlp_utils, self.unit_converter),
            DuplicateStrategy4_DomainSynonymDetection(self.nlp_utils, self.unit_converter),
        ]

        self.inconsistency_strategies = [
            InconsistencyStrategy1_SimilarDifferentValues(self.nlp_utils, self.unit_converter),
            InconsistencyStrategy2_RangeOverlap(self.nlp_utils, self.unit_converter),
            InconsistencyStrategy3_VaguenessDetection(self.nlp_utils, self.unit_converter),
            InconsistencyStrategy4_PartialScopeMismatch(self.nlp_utils, self.unit_converter),
            InconsistencyStrategy5_MathematicalConsistency(self.nlp_utils, self.unit_converter),
        ]

        self.conflict_strategies = [
            ConflictStrategy1_NLIContradiction(self.nlp_utils, self.unit_converter),
            ConflictStrategy2_DomainAntonymDetection(self.nlp_utils, self.unit_converter),
            ConflictStrategy4_ImplicitNegation(self.nlp_utils, self.unit_converter),
            ConflictStrategy5_ExceptionPattern(self.nlp_utils, self.unit_converter),
            ConflictStrategy6_DirectNegation(self.nlp_utils, self.unit_converter),
        ]

        self.conflict_disqualifiers = [
            ConflictStrategy3_DynamicEntityMismatch(self.nlp_utils, self.unit_converter),
        ]

        print("Loading transformer models...")
        self.embedding_model = SentenceTransformer('sentence-transformers/all-MiniLM-L6-v2')
        self.nli_model = pipeline('text-classification',
                                 model='facebook/bart-large-mnli',
                                 device=-1)
        print("All models loaded!\n")

    def set_domain(self, domain: str):
        """Change the active domain"""
        self.nlp_utils.set_domain(domain)
        print(f"Domain set to: {domain}")

    def analyze_pair(self, req1: str, req2: str, i: int, j: int) -> List[Detection]:
        # ========== PRIORITY 1: UNIT CONVERSION CHECK (BEFORE ANY OTHER ANALYSIS) ==========
        result1 = self.unit_converter.extract_value_unit(req1)
        result2 = self.unit_converter.extract_value_unit(req2)

        if result1 and result2:
            value1, unit1 = result1
            value2, unit2 = result2

            # Debug output
            # print(f"DEBUG: Checking units - {value1}{unit1} vs {value2}{unit2}")

            if self.unit_converter.are_equivalent(value1, unit1, value2, unit2):
                # CONFIRMED DUPLICATE - Return immediately, don't run NLI
                converted_value = self.unit_converter.convert(value1, unit1, unit2)
                # print(f"DEBUG: ✓ Units are equivalent!")
                return [Detection(
                    i, j, req1, req2, IssueType.DUPLICATE, 0.98,
                    {
                        'method': 'dynamic_unit_conversion',
                        'value1': f"{value1} {unit1}",
                        'value2': f"{value2} {unit2}",
                        'converted': f"{value1} {unit1} = {converted_value} {unit2}",
                        'library': 'pint',
                        'equivalent': True
                    }
                )]

        # ========== PRIORITY 2: SEMANTIC ANALYSIS ==========
        embeddings = self.embedding_model.encode([req1, req2])
        cosine_sim = cosine_similarity([embeddings[0]], [embeddings[1]])[0][0]

        text = f"{req1} [SEP] {req2}"
        nli_result = self.nli_model(text)[0]

        keywords1 = self.nlp_utils.extract_semantic_keywords(req1)
        keywords2 = self.nlp_utils.extract_semantic_keywords(req2)
        semantically_related = bool(keywords1 & keywords2) or cosine_sim > 0.3

        context = {
            'cosine_sim': cosine_sim,
            'nli_label': nli_result['label'],
            'nli_score': nli_result['score'],
            'semantically_related': semantically_related
        }

        detections = []

        # ========== PRIORITY 2: INCONSISTENCIES ==========
        for strategy in self.inconsistency_strategies:
            detected, confidence, evidence = strategy.detect(req1, req2, context)
            if detected and confidence >= 0.70:
                detections.append(Detection(
                    i, j, req1, req2, IssueType.INCONSISTENCY, confidence, evidence
                ))
                return detections

        # ========== PRIORITY 3: CONFLICTS (High Priority) ==========
        conflict_disqualified = False
        for disqualifier in self.conflict_disqualifiers:
            disqualified, _, _ = disqualifier.detect(req1, req2, context)
            if disqualified:
                conflict_disqualified = True
                break

        if not conflict_disqualified:
            for strategy in self.conflict_strategies:
                detected, confidence, evidence = strategy.detect(req1, req2, context)
                if detected and confidence >= 0.70:
                    detections.append(Detection(
                        i, j, req1, req2, IssueType.CONFLICT, confidence, evidence
                    ))
                    return detections

        # ========== PRIORITY 4: OTHER DUPLICATE STRATEGIES ==========
        for strategy in self.duplicate_strategies:
            if isinstance(strategy, DuplicateStrategy3_DynamicUnitConversion):
                continue  # Already handled above
            detected, confidence, evidence = strategy.detect(req1, req2, context)
            if detected and confidence >= 0.70:
                detections.append(Detection(
                    i, j, req1, req2, IssueType.DUPLICATE, confidence, evidence
                ))
                return detections

        return detections

    def analyze_requirements(self, requirements: List[str], verbose=True, auto_detect_domain=True):
        # Auto-detect domain if requested
        if auto_detect_domain and self.domain == 'auto':
            detected_domain = DomainKnowledge.detect_domain(requirements)
            self.set_domain(detected_domain)
            print(f"📍 Auto-detected domain: {detected_domain.upper()}\n")

        all_detections = []

        if verbose:
            print("=" * 80)
            print("ANALYZING REQUIREMENTS")
            print("=" * 80)

        for i in range(len(requirements)):
            for j in range(i + 1, len(requirements)):
                detections = self.analyze_pair(requirements[i], requirements[j], i, j)

                if verbose and detections:
                    print(f"\n🔍 Req {i} vs Req {j}")
                    print(f"   Req {i}: {requirements[i]}")
                    print(f"   Req {j}: {requirements[j]}")
                    for detection in detections:
                        symbol = {"duplicate": "✓", "conflict": "✗", "inconsistency": "⚠"}[detection.issue_type.value]
                        print(f"   {symbol} {detection.issue_type.value.upper()} (confidence: {detection.confidence:.2f})")
                        print(f"      Evidence: {detection.evidence}")

                all_detections.extend(detections)

        return all_detections

    def print_summary(self, detections: List[Detection]):
        duplicates = [d for d in detections if d.issue_type == IssueType.DUPLICATE]
        conflicts = [d for d in detections if d.issue_type == IssueType.CONFLICT]
        inconsistencies = [d for d in detections if d.issue_type == IssueType.INCONSISTENCY]

        print("\n" + "=" * 80)
        print("SUMMARY")
        print("=" * 80)
        print(f"\n✓ DUPLICATES: {len(duplicates)}")
        for d in duplicates:
            print(f"   Req {d.req1_id} ↔ Req {d.req2_id} (confidence: {d.confidence:.2f})")

        print(f"\n✗ CONFLICTS: {len(conflicts)}")
        for d in conflicts:
            print(f"   Req {d.req1_id} ↔ Req {d.req2_id} (confidence: {d.confidence:.2f})")

        print(f"\n⚠ INCONSISTENCIES: {len(inconsistencies)}")
        for d in inconsistencies:
            print(f"   Req {d.req1_id} ↔ Req {d.req2_id} (confidence: {d.confidence:.2f})")

        print(f"\n📊 Total Issues: {len(detections)}")

